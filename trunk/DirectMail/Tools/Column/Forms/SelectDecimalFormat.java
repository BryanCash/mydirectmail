  private void showExample() {
    thousandsSep = (String) combo_thousands.getSelectedItem();
    decimalSep = (String) combo_decimal.getSelectedItem();
    try {
      numOfDigits = Integer.parseInt((String) combo_digits.getSelectedItem());
    } catch (NumberFormatException ex) {
      MainForm.myLog.log(Level.WARNING, "Number of digits should be an integer");
      numOfDigits = 2;
      combo_digits.setSelectedItem("2");
    }
    pattern = "#,##0";
    if (numOfDigits > 0) {
      pattern += ".";
      for (int i = 0; i < numOfDigits; i++) {
        pattern += "0";
      }
    }
    dfs = new DecimalFormatSymbols();
    char thSep = thousandsSep.charAt(0);
    if(thSep == '('){
      thSep = '\0';
    }
    char decSep = decimalSep.charAt(0);
    dfs.setGroupingSeparator(thSep);
    dfs.setDecimalSeparator(decSep);
    decFormat = new DecimalFormat(pattern);
    decFormat.setDecimalFormatSymbols(dfs);
    label_example.setText(decFormat.format(dNumber).replaceAll(" ", ""));
  }
