    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

            if(m.getAddressField() == -1 && checkAddress.isSelected()){
                soldatos.messages.Messages.customError("No Address Field selected", "You haven't marked an address field");
                return;
            }
            if(m.getTkField() == -1 && checkTk.isSelected()){
                soldatos.messages.Messages.customError("No Address Field selected", "You haven't marked an tk field");
                return;
            }
            List13 l;
            try {
                l = new List13(m,checkAddress.isSelected(),checkTk.isSelected());
                Thread t = new Thread(l);
                t.start();
                dispose();
            } catch (IOException ex) {
                MainForm.myLog.log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                MainForm.myLog.log(Level.SEVERE, null, ex);
            } 

       
    }//GEN-LAST:event_jButton1ActionPerformed

  
