  private void comboPostActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboPostActionPerformed
    if (comboPost.getSelectedItem().equals("≈À‘¡")) {
      calendarPanel.setVisible(false);
      labelCenter.setVisible(false);
      textFieldCenter.setVisible(false);
      setSize(sDim);
      elta = true;
      infoPost = false;
      pressPost = false;
      comboAdv.setEnabled(true);
    } else if (comboPost.getSelectedItem().equals("INFOPOST")) {
      calendarPanel.setVisible(true);
      labelCenter.setVisible(true);
      textFieldCenter.setVisible(true);
      setSize(eDim);
      elta = false;
      infoPost = true;
      pressPost = false;
      comboAdv.setEnabled(false);
    } else if (comboPost.getSelectedItem().equals("PRESSPOST")) {
      calendarPanel.setVisible(true);
      labelCenter.setVisible(true);
      textFieldCenter.setVisible(true);
      setSize(eDim);
      elta = false;
      infoPost = false;
      pressPost = true;
      comboAdv.setEnabled(false);
    }
}//GEN-LAST:event_comboPostActionPerformed

