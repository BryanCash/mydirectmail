  private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
    addBarcode = checkBoxAddBarcode.isSelected();
    adv = (String) comboAdv.getSelectedItem();
    if (infoPost || pressPost) {
      sDate = txtDateChooser.getText();
      //sDate = dateFormated.format(this.txtDateChooser.getDate());
      center = textFieldCenter.getText();
      System.out.println(sDate);
    }
    try {
      sortMin = Integer.parseInt(textFieldSortMin.getText());
      packMax = Integer.parseInt(textFieldPackMax.getText());
      TKLabels tkl = new TKLabels(sortMin, packMax, addBarcode, elta, infoPost, pressPost, center, sDate, adv, m);
      Thread t = new Thread(tkl);
      t.start();
      dispose();
    } catch (NumberFormatException nFE) {
      Errors.customError("Not a valid number", "The number you entered is not an integer");
      textFieldSortMin.setText("");
      textFieldPackMax.setText("");
    } catch (Exception ex) {
      MainForm.myLog.log(Level.WARNING, null, ex);
    }

  }//GEN-LAST:event_okButtonActionPerformed

