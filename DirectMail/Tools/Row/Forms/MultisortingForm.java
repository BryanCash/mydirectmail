  private String getSelectedButton(ButtonGroup bg){
    for (Enumeration e=bg.getElements(); e.hasMoreElements(); ) { 
      JRadioButton b = (JRadioButton)e.nextElement(); 
      if (b.getModel() == bg.getSelection()){ 
        return b.getName();
      } 
    }


    return "";
  }


