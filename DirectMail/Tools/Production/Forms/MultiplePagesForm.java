  private void button_okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_okActionPerformed
    pages = Integer.parseInt(String.valueOf(combo_pages.getSelectedItem()));
    String[] duplexStr = textfield_duplex.getText().split(",");
    duplex = new int[duplexStr.length];
    m.setCounterField(combo_counterField.getSelectedIndex());
    for (int i = 0; i < duplexStr.length; i++) {
      if (!duplexStr[i].equals("")) {
        if (StringFunctions.isInteger(duplexStr[i])) {
          duplex[i] = Integer.parseInt(duplexStr[i]);
        } else {
          soldatos.messages.Messages.customError("Not a number", "Page " + duplexStr[i] + " is not a number");
        }
      }
    }
    dispose();
  }//GEN-LAST:event_button_okActionPerformed
