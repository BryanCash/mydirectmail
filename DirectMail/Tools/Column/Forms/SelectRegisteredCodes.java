    private void button_okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_okActionPerformed
      ValidationGroup group = new ValidationGroup();
      group.addComponent(tf_from);
      group.addComponent(tf_to);
      group.addComponent(combo_suffix);
      if (!group.validate()) {
        group.errorMessage();
        return;
      }
      way = NUMBERS;
      from = Long.parseLong(tf_from.getText());
      to = Long.parseLong(tf_to.getText());
      prefix = combo_prefix.getSelectedItem().toString();
       suffix = combo_suffix.getSelectedItem().toString();
      dispose();
    }//GEN-LAST:event_button_okActionPerformed

    private void tf_fromKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tf_fromKeyReleased
      compare.setValueToCompareWith(tf_to.getText().trim());
      tf_from.addValidator(compare);
      tf_from.validateValue();
      tf_to.validateValue();
    }//GEN-LAST:event_tf_fromKeyReleased

